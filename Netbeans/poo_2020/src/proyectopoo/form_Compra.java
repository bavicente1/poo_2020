    private void btn_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_BuscarActionPerformed
        //
        try {
            boolean exist = false;
            int id = Integer.parseInt(txt_Id.getText());
            int id2 ;
            for (Object producto : listaProductos) {
                //Guarda el id de cada producto de la lista en id2
                Method metodoGetID = producto.getClass().getMethod("getId");
                id2 = (int) metodoGetID.invoke(producto);
                if (id == id2) {
                    exist = true;
                    //Guarda los datos del objeto encontrado y para ingresarlos en la interfaz
                    String nombreProducto = (String) producto.getClass().getMethod("getNombre").invoke(producto);
                    int stock = (int) producto.getClass().getMethod("getStock").invoke(producto);
                    double valor = (double) producto.getClass().getMethod("getValor").invoke(producto);
                    String tipoProducto = (String) producto.getClass().getMethod("getTipoProducto").invoke(producto);
                    //los introduce en la interfaz
                    cb_TipoProducto.setSelectedItem(tipoProducto);

                    lbl_Valor_cu.setText(String.valueOf(valor));
                    lbl_Stock.setText(String.valueOf(stock));
                    //Borra todos los componentes del cb_producto y añade el introducido por la ID
                    cb_Producto.removeAllItems();
                    cb_Producto.addItem(nombreProducto);
                    cb_Producto.setSelectedItem(producto);
                    //Guarda el producto encontrado en una variable para su próximo uso
                    productoTemp = producto;
                    break;
                }
            }
            //Si id2 == -1 significa que no existe La Id buscada por lo que envia un aviso
            if (!exist) {
                lbl_AVISO.setText("ID No encontrada");
                lbl_AVISO.setVisible(true);
            } else {

                lbl_AVISO.setVisible(false);
            }
        } catch (Exception e) {
            System.out.println("Formulario de compra error: " + e);
            lbl_AVISO.setText("Ingrese una ID válida");
            lbl_AVISO.setVisible(true);
        }


    }//GEN-LAST:event_btn_BuscarActionPerformed

