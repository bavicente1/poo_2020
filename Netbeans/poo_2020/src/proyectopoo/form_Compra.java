    private void btn_EliminarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EliminarProductoActionPerformed
        int fila = tabla_Factura.getSelectedRow();

        if (fila != -1) {
            try {
                int cantidad = Integer.parseInt(listaCompra.get(fila)[2]);
                
                int id = Integer.parseInt(listaCompra.get(fila)[0]);
                int id2;
                //Busca el objeto en la lista de productos y le duevelve la cantidad a su stock
                for (Object producto : listaProductos) {

                    Method metodoGetID = producto.getClass().getMethod("getId");
                    id2 = (int) metodoGetID.invoke(producto);
                    if (id == id2) {
                        
                        int stock = (int) producto.getClass().getMethod("getStock").invoke(producto);
                        producto.getClass().getMethod("setStock", int.class).invoke(producto, (stock + cantidad));
                        break;
                    }
                }

                //Actualiza los datos de la lista y la tabla
                listaCompra.remove(fila);
                modelTab.removeRow(fila);

                //actualiza los precios
                ActualizarFactura();
            } catch (Exception e) {
                System.out.println("btn_Eliminar" + e);

            }
        }


    }//GEN-LAST:event_btn_EliminarProductoActionPerformed

