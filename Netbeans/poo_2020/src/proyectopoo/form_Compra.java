    private void ActualizarFactura() {
        double subTotal = 0;
        double iva = 0;
        double total = 0;
        for (int i = 0; i < listaCompra.size(); i++) {
            double precio = Double.parseDouble(listaCompra.get(i)[3]);
            double cant = Double.parseDouble(listaCompra.get(i)[2]);
            subTotal = subTotal + (precio * cant);

        }
        iva = subTotal * 0.12;
        total = subTotal + iva;

        lbl_Subtotal.setText(String.format("%.2f", subTotal));
        lbl_Iva.setText(String.format("%.2f", iva));
        lbl_Total.setText(String.format("%.2f", total));
    }

